Data ='data.xlsx';
data=xlsread(Data);
D=data(:,1);
L=data(:,2);
P=data(:,3);
G=data(:,4);
deltah=data(:,5);
X=data(:,6);
qc=data(:,7);
minD=min(D);
maxD=max(D);
minL=min(L);
maxL=max(L);
minP=min(P);
maxP=max(P);
minG=min(G);
maxG=max(G);
mindeltah=min(deltah);
maxdeltah=max(deltah);
minX=min(X);
maxX=max(X);
minqc=min(qc);
maxqc=max(qc);
Dn=(D-minD)/(maxD-minD);
Ln=(L-minL)/(maxL-minL);
Pn=(P-minP)/(maxP-minP);
Gn=(G-minG)/(maxG-minG);
deltahn=(deltah-mindeltah)/(maxdeltah-mindeltah);
Xn=(X-minX)/(maxX-minX);
qcn=(qc-minqc)/(maxqc-minqc);
dataCHF=[Dn,Ln,Pn,Gn,deltahn,Xn,qcn];
